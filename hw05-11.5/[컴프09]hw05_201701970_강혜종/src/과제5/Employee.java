package 과제5;

public class Employee {
	String 이름;
	String 전화번호;
	String 연봉;
	
	public String get이름(){
		return 이름;
	}
	
	public void set이름(String a){
		이름 = a;
	}
	
	public String get전화번호(){
		return 전화번호;
	}
	
	public void set전화번호(String b){
		전화번호 = b;
	}
	
	public String get연봉(){
		return 연봉;
	}
	
	public void set연봉(String c){
		연봉 = c;
	}
	
}
